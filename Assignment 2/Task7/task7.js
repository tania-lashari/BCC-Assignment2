var firstnumber = prompt();
var secondnumber = prompt();
var operation = prompt();


if(operation = "-" ){
    console.log(firstnumber-secondnumber);
    

}
else if(operation = "+"){
    console.log(firstnumber+secondnumber);
    

}
else if(operation = "/"){
    console.log(firstnumber/secondnumber);
    

}
else if(operation = "*"){
    console.log(firstnumber*secondnumber);

}